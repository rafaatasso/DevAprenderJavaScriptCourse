// Strict equality
// === -> compare value and type

console.log( 1 === 1);      // true
console.log( 1 === '1');    // false

// Loose equality
// == -> compare just value, because the software converte for the same types
// NOT recommended  

console.log( 1 == 1);      // true
console.log( 1 == '1');    // true