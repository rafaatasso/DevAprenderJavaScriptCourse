let salary = 100;

// Add +
// Subtract -
// Division /
// Multiply *
// Exponential **

console.log(salary + salary);
console.log(salary - salary);
console.log(salary / salary);
console.log(salary * salary);
console.log(2 ** 4);

// Increment ++
// Decrement --

let age = 18;

console.log(age++); // In this way, the computer will show the result and then increment the number
console.log(age);
console.log(++age); // In this way, the computer will increment the number and then show the result